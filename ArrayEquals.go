package utils

// ArrayEquals checks whether 2 string arrays are the same
func ArrayEqualsStr(a, b []string) bool {
	if len(a) != len(b) {
		return false
	}
	for i := 0; i < len(a); i++ {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}

// ArrayEquals checks whether 2 string arrays are the same
func ArrayEqualsInt(a []int, b []int) bool {
	if len(a) != len(b) {
		return false
	}
	for i, v := range a {
		if v != b[i] {
			return false
		}
	}
	return true
}
